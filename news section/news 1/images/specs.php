<div id="review-body" class="review-body clearfix">

<p>Tecnoâs latest <a href="tecno_pova_3-11553.php">Pova 3</a> brings a large 7,000 mAh battery alongside a 6.9-inch 90Hz display and a 50MP main camera. The phone was announced in the Philippines and is expected to make its way to more markets soon.</p>

<img class="inline-image" width="1200" height="700" src="https://fdn.gsmarena.com/imgroot/news/22/05/tecno-pova-3-ofic/-1200/gsmarena_003.jpg" alt="Tecno Pova 3 announced with 90Hz LCD and 7,000 mAh battery">

<p>The key selling point here is the massive battery. At 7,000 mAh, Tecno is confident youâll manage at least two days so usage before needing to recharge. The phone also comes with several battery enhancements including a Super Power Saving Mode and a reverse charge function so you can charge other devices at up to 10W speeds. Pova 3 supports up to 25W charging speeds though it comes bundled with a 33W charger in the box.</p>

<img class="inline-image" width="1200" height="675" src="https://fdn.gsmarena.com/imgroot/news/22/05/tecno-pova-3-ofic/-1200/gsmarena_002.jpg" alt="Tecno Pova 3 announced with 90Hz LCD and 7,000 mAh battery">

<p>Elsewhere, the device comes with a 6.9-inch LCD with FHD+ resolution and a 90Hz refresh rate. Thereâs an 8MP selfie shooter and a 50MP main cam around the back alongside two 2MP auxiliary sensors. MediaTekâs Helio G88 sits at the helm aided by 4/6GB RAM and 64/128GB of expandable storage.</p>

<p>The phone is also equipped with stereo speakers, a side-mounted fingerprint scanner, a headphone jack and an FM radio receiver. The software side is covered by HiOS on top of Android 11.</p>

<img class="inline-image" width="1200" height="675" src="https://fdn.gsmarena.com/imgroot/news/22/05/tecno-pova-3-ofic/-1200/gsmarena_001.jpg" alt="Tecno Pova 3 announced with 90Hz LCD and 7,000 mAh battery">

<p>Tecno Pova 3 comes in Electric Blue, Tech Silver and Eco Black colors. In the Philippines, the baseline 4/64GB trim is priced at PHP 8,999 ($172) while the 6/128GB model is going for PHP 9,399 ($180).</p>

